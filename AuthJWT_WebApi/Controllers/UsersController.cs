        public UsersController(AuthJWT_WebApiContext context, IUserService userService)
        {
            _context = context;
            _userService = userService;
        }

        [AllowAnonymous]
        [HttpPost("authenticate")]
        public IActionResult Authenticate([FromBody]AuthenticateModel model)
        {
            List<User> User_List = _context.User.ToList();
            var user = _userService.Authenticate(model.Username, model.Password, User_List);

            if (user == null)
                return BadRequest(new { message = "Username or password is incorrect" });

            return Ok(user);
        }

        [HttpGet]
        public IActionResult GetAll()
        {
            List<User> User_List = _context.User.ToList();
            var users = _userService.GetAll(User_List);
            return Ok(users);
        }

